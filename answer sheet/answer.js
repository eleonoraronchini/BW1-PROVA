const btn1 = document.getElementById('btn1')
btn1.addEventListener('click', function(){
   
    const p1 = document.getElementById('p1')
    let p1R = document.createComment('CPU')
    p1.appendChild(p1R)
    let p1Result = document.getElementById('p1')
    p1Result.innerText = 'Central Processing Unit'
    p1Result.style.color = 'magenta'
    p1Result,this.style.textShadow = '0 0 2em white'
    p1Result.style.font = 'Outfit'
    p1Result.style.fontSize = '2rem'
    p1Result.style.fontWeight = '700'

    btn1.addEventListener('click', function(){
        p1Result.remove()
    })
})

